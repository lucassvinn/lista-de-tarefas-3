#include <stdio.h>

int main()
{
    int numeros[5] = {10, 20, 30, 40, 50};
    float notas[3] = {5.5, 3.6, 8.9};
    char codigo[4] = {'a', 'b', 'c', 'd', 'e'};
    
    printf("Número[0] = %d\n", numeros[0]);
    printf("Notas[1] = %f\n", notas[1]);
    printf("Codígo[0] = %c", codigo[0]);

    
    return 0;
}
